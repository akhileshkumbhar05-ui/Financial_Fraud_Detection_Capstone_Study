def norm01(a):
    a = np.asarray(a, dtype=np.float64)
    return (a - np.min(a)) / (np.ptp(a) + 1e-12)

def recall_at_top_pct(y_true, scores, pct):
    n = max(1, int(np.ceil(len(y_true)*pct/100.0)))
    idx = np.argsort(-scores)[:n]
    return y_true[idx].sum() / max(1, y_true.sum())

