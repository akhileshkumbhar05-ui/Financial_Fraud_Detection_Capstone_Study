def tune_if(Xtr,ytr,Xv,yv,trials=3):
    rng=np.random.default_rng(SEED); best=(-1,None)
    for _ in range(trials):
        n_est=int(rng.choice([200,300,400])); maxs=rng.choice([1.0,0.7])
        mss="auto" if maxs==1.0 else int(maxs*len(Xtr))
        clf=IsolationForest(n_estimators=n_est, max_samples=mss, random_state=SEED, n_jobs=-1)
        clf.fit(Xtr)
        ap=average_precision_score(yv, norm01(-clf.score_samples(Xv)))
        if ap>best[0]: best=(ap,clf)
    return best[1]

def tune_lof(Xtr,ytr,Xv,yv,trials=3):
    rng=np.random.default_rng(SEED); best=(-1,None)
    for _ in range(trials):
        k=int(rng.choice([20,30,40])); metric=str(rng.choice(["euclidean","manhattan"]))
        clf=LocalOutlierFactor(n_neighbors=k, novelty=True, metric=metric)
        clf.fit(Xtr)
        ap=average_precision_score(yv, norm01(-clf.decision_function(Xv)))
        if ap>best[0]: best=(ap,clf)
    return best[1]

class AE(nn.Module):
    def __init__(self,d, bottleneck=24):
        super().__init__()
        self.enc=nn.Sequential(nn.Linear(d,128), nn.ReLU(), nn.Dropout(0.1),
                               nn.Linear(128,64), nn.ReLU(),
                               nn.Linear(64,bottleneck))
        self.dec=nn.Sequential(nn.Linear(bottleneck,64), nn.ReLU(),
                               nn.Linear(64,128), nn.ReLU(),
                               nn.Linear(128,d))
    def forward(self,x): z=self.enc(x); return self.dec(z)

def train_ae(Xtr, epochs=6):
    ae=AE(Xtr.shape[1]).to(device)
    opt=torch.optim.Adam(ae.parameters(),lr=1e-3,weight_decay=1e-5); crit=nn.MSELoss()
    for ep in range(epochs):
        idx=np.random.permutation(len(Xtr))
        for i in range(0,len(idx),2048):
            xb=torch.tensor(Xtr[idx[i:i+2048]],dtype=torch.float32,device=device)
            opt.zero_grad(set_to_none=True); rec=ae(xb); loss=crit(rec,xb); loss.backward(); opt.step()
    @torch.no_grad()
    def score(X):
        s=[]
        for i in range(0,len(X),2048):
            xb=torch.tensor(X[i:i+2048],dtype=torch.float32,device=device)
            e=((ae(xb)-xb)**2).mean(1).detach().cpu().numpy(); s.append(e)
        return norm01(np.concatenate(s))
    return ae, score

