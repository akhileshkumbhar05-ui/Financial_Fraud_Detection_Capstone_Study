class Encoder(nn.Module):
    def __init__(self,in_dim,emb):
        super().__init__()
        self.net = nn.Sequential(nn.Linear(in_dim,128), nn.ReLU(),
                                 nn.Linear(128,64),  nn.ReLU(),
                                 nn.Linear(64,emb))
        self.ln  = nn.LayerNorm(emb)
    def forward(self,x): return self.ln(self.net(x))

def pairwise_dist(x):
    xn=(x**2).sum(1,keepdim=True); return xn + xn.T - 2*(x@x.T)

def batch_hard_triplet_loss(emb,y,margin=0.2):
    B,dev=emb.size(0),emb.device
    with torch.no_grad():
        D_=pairwise_dist(emb)
        same=y[:,None].eq(y[None,:]); diag=torch.eye(B,dtype=torch.bool,device=dev)
        Dp=D_.clone(); Dp[~same|diag]=-1e9; pos=Dp.argmax(1)
        Dn=D_.clone(); Dn[same]=1e9;  neg=Dn.argmin(1)
    D=pairwise_dist(emb); ar=torch.arange(B,device=dev)
    hp,hn=D[ar,pos], D[ar,neg]
    return F.relu(margin + hp - hn).mean()

def make_balanced_batches(X,y,batch,steps):
    pos=np.where(y==1)[0]; neg=np.where(y==0)[0]
    rng=np.random.default_rng(SEED)
    for _ in range(steps):
        pi=rng.choice(pos,size=max(1,batch//2),replace=True)
        ni=rng.choice(neg,size=batch-len(pi),replace=False)
        idx=np.concatenate([pi,ni]); rng.shuffle(idx)
        yield idx

@torch.no_grad()
def embed(encoder, X):
    encoder.eval()
    xb=torch.tensor(X,dtype=torch.float32,device=device)
    out=[]
    for i in range(0,len(xb),8192):
        out.append(encoder(xb[i:i+8192]).detach().cpu())
    return torch.cat(out,0).numpy()

