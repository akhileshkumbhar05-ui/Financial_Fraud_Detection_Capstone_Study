def _rate_and_size(ids, y):
    ids = ids.astype(int)
    K = (ids.max()+1) if ids.size else 0
    size = np.bincount(ids.clip(min=0), minlength=K)
    fraud = np.bincount(ids.clip(min=0), minlength=K, weights=y)
    rate = np.divide(fraud, np.maximum(1, size), where=(size>0))
    return size, rate

