import numpy as np, torch
try:
    import faiss  # CPU or GPU module
    _FAISS_AVAILABLE = True
    def _faiss_has_gpu():
        try:
            return (torch.cuda.is_available() and
                    hasattr(faiss, "StandardGpuResources") and
                    hasattr(faiss, "index_cpu_to_all_gpus") and
                    hasattr(faiss, "get_num_gpus") and
                    faiss.get_num_gpus() > 0)
        except Exception:
            return False
    _FAISS_HAS_GPU = _faiss_has_gpu()
except Exception:
    faiss = None
    _FAISS_AVAILABLE = False
    _FAISS_HAS_GPU = False

def _knn_sklearn(query, ref, k):
    from sklearn.neighbors import NearestNeighbors
    k = min(k, len(ref))
    nn = NearestNeighbors(n_neighbors=k, n_jobs=-1).fit(ref)
    D, I = nn.kneighbors(query, n_neighbors=k, return_distance=True)
    return D, I

def _faiss_knn(query, ref=None, k=10, metric="L2", use_gpu=True):
    q = np.ascontiguousarray(query.astype("float32"))
    r = q if ref is None else np.ascontiguousarray(ref.astype("float32"))
    k = min(k, len(r))
    if not _FAISS_AVAILABLE:
        return _knn_sklearn(q, r, k)
    index = faiss.IndexFlatL2(r.shape[1]) if metric == "L2" else faiss.IndexFlatIP(r.shape[1])
    if use_gpu and _FAISS_HAS_GPU:
        index = faiss.index_cpu_to_all_gpus(index)
    index.add(r)
    D, I = index.search(q, k)
    return D, I

def build_mutual_knn(Z, k=K_GRAPH):
    _, idx = _faiss_knn(Z, None, k=k, metric="L2", use_gpu=True)
    N,K = idx.shape
    src = np.repeat(np.arange(N,dtype=np.int64),K)
    dst = idx.ravel().astype(np.int64)
    pairs   = np.ascontiguousarray(np.stack([src,dst],1))
    pairs_r = np.ascontiguousarray(pairs[:,::-1])
    st=np.dtype([('i',np.int64),('j',np.int64)])
    inter = np.intersect1d(pairs.view(st).reshape(-1), pairs_r.view(st).reshape(-1))
    mut = inter.view(np.int64).reshape(-1,2)
    s = np.concatenate([mut[:,0], mut[:,1]])
    d = np.concatenate([mut[:,1], mut[:,0]])
    return s,d

def build_temporal_knn_to_train(Zq, Ztr, Tq, Ttr, k=K_GRAPH, dt_max=DT_MAX, oversample=8):
    Kc = min(max(k*oversample, k), len(Ztr))
    _, idx = _faiss_knn(Zq, Ztr, k=Kc, metric="L2", use_gpu=True)
    srcL, dstL = [], []
    for i, cand in enumerate(idx):
        mask = (Ttr[cand] <= Tq[i])
        if dt_max is not None:
            mask = mask & ((Tq[i] - Ttr[cand]) <= dt_max)
        sel = cand[mask][:k]
        if sel.size==0: continue
        srcL.append(np.full(sel.size, i, dtype=np.int64)); dstL.append(sel.astype(np.int64))
    if not srcL: return np.array([],dtype=np.int64), np.array([],dtype=np.int64)
    return np.concatenate(srcL), np.concatenate(dstL)

# edge cache
_edge_cache={}
def _zhash(Z):
    m=hashlib.sha1(); m.update(Z.shape.__repr__().encode()); m.update(np.float32(Z[:min(len(Z),256)]).tobytes()); return m.hexdigest()
def cached_mutual_knn(Z,k):
    key=("mutual",k,_zhash(Z));
    if key in _edge_cache: return _edge_cache[key]
    e=build_mutual_knn(Z,k); _edge_cache[key]=e; return e
def cached_temporal_to_train(Zq,Ztr,Tq,Ttr,k,dt):
    key=("temporal",k,dt,_zhash(Zq),_zhash(Ztr))
    if key in _edge_cache: return _edge_cache[key]
    e=build_temporal_knn_to_train(Zq,Ztr,Tq,Ttr,k,dt); _edge_cache[key]=e; return e

# GraphSAGE
class SAGE(nn.Module):
    def __init__(self,in_dim,h=64,drop=0.2):
        super().__init__()
        self.s1=SAGEConv(in_dim,h); self.s2=SAGEConv(h,h)
        self.drop=nn.Dropout(drop); self.lin=nn.Linear(h,1)
    def forward(self,x,ei):
        x=F.relu(self.s1(x,ei)); x=self.drop(x)
        x=F.relu(self.s2(x,ei)); x=self.drop(x)
        return self.lin(x).squeeze(-1)

def focal_bce(logits, targets, pos_weight, gamma=2.0):
    bce = F.binary_cross_entropy_with_logits(logits, targets, reduction='none', pos_weight=pos_weight)
    p = torch.sigmoid(logits); pt = torch.where(targets==1, p, 1-p)
    return ((1-pt)**gamma * bce).mean()

def train_gnn_conf(Xtr,ytr,Xv,yv,Ttr,Tv, graph_mode, emb_dim, h, drop, gamma, lr, k=K_GRAPH):
    # Triplet encoder
    enc=Encoder(Xtr.shape[1],emb_dim).to(device)
    opt=torch.optim.Adam(enc.parameters(),lr=1e-3,weight_decay=1e-4)
    scaler=torch.cuda.amp.GradScaler(enabled=AMP)
    steps=int(np.ceil(len(ytr)/BATCH_TRIP))
    best, bad, pat = 1e9, 0, 4
    tic("Triplet")
    for ep in range(TRIP_EPOCHS):
        enc.train(); losses=[]
        for idx in make_balanced_batches(Xtr,ytr,BATCH_TRIP,steps):
            xb=torch.tensor(Xtr[idx],dtype=torch.float32,device=device)
            yb=torch.tensor(ytr[idx],dtype=torch.long,device=device)
            opt.zero_grad(set_to_none=True)
            with torch.cuda.amp.autocast(enabled=AMP):
                z=enc(xb); loss=batch_hard_triplet_loss(z,yb,margin=0.2)
            scaler.scale(loss).backward(); scaler.step(opt); scaler.update()
            losses.append(loss.item())
        ml=float(np.mean(losses))
        if ml<best-1e-4: best,bad=ml,0
        else:
            bad+=1
            if bad>=pat: break
    toc("Triplet done")

    tic("Embeddings")
    Ztr = embed(enc, Xtr); Zv = embed(enc, Xv)
    toc("Embeddings done")

    tic("Graphs")
    s_tr,d_tr = cached_mutual_knn(Ztr,k)
    if graph_mode=="temporal_knn":
        s_v,d_v = cached_temporal_to_train(Zv,Ztr,Tv,Ttr,k,DT_MAX)
    else:
        nnm=NearestNeighbors(n_neighbors=min(k,len(Ztr))).fit(Ztr)
        _, idxv = nnm.kneighbors(Zv)
        s_v=np.repeat(np.arange(len(Zv)), idxv.shape[1]); d_v=idxv.ravel().astype(np.int64)
    toc("Graphs built")

    x_tr=torch.tensor(Ztr,dtype=torch.float32,device=device)
    y_tr_t=torch.tensor(ytr,dtype=torch.long,device=device)
    ei_tr=torch.tensor([s_tr,d_tr],dtype=torch.long,device=device)

    x_all=torch.tensor(np.vstack([Ztr,Zv]),dtype=torch.float32,device=device)
    y_all=torch.tensor(np.concatenate([ytr,yv]),dtype=torch.long,device=device)
    ei_val=torch.cat([ei_tr, torch.tensor([s_v+len(Ztr), d_v],dtype=torch.long,device=device)], dim=1)
    val_mask=torch.zeros(x_all.size(0),dtype=torch.bool,device=device); val_mask[len(Ztr):]=True

    model=SAGE(in_dim=x_tr.size(1),h=h,drop=drop).to(device)
    pos_w=torch.tensor((len(ytr)-ytr.sum())/(ytr.sum()+1e-9),device=device).float()
    optm=torch.optim.Adam(model.parameters(),lr=lr,weight_decay=1e-4)
    scaler=torch.cuda.amp.GradScaler(enabled=AMP)

    best_ap=-1; bad=0; pat=5
    tic("SAGE")
    for ep in range(GNN_EPOCHS):
        model.train(); optm.zero_grad(set_to_none=True)
        with torch.cuda.amp.autocast(enabled=AMP):
            logits=model(x_tr, ei_tr)
            loss=focal_bce(logits, y_tr_t.float(), pos_w, gamma=gamma)
        scaler.scale(loss).backward(); scaler.step(optm); scaler.update()
        model.eval()
        with torch.no_grad(), torch.cuda.amp.autocast(enabled=AMP):
            pv=torch.sigmoid(model(x_all, ei_val))[val_mask].float().cpu().numpy()
        ap=average_precision_score(yv,pv)
        print(f"[SAGE] ep{ep:02d} loss={loss.item():.4f} AP(val)={ap:.4f}")
        if ap>best_ap+1e-4:
            best_ap, best_state, bad = ap, {k:v.detach().cpu() for k,v in model.state_dict().items()}, 0
        else:
            bad+=1
            if bad>=pat: break
    toc("SAGE done")
    model.load_state_dict(best_state)
    return enc, model, best_ap

